LoadFont::
	ld a, c
	add a, a
	jr nc, .loadToBank0
	ld l, a
	ld a, 1
	ld [rVBK], a
	ld a, l
.loadToBank0
	add a, FontPointers & $FF
	ld l, a
	adc FontPointers >> 8
	sub l
	ld h, a
	
	ld a, [hli]
	ld h, [hl]
	ld l, a
	
	ld de, vFontTiles
	ld bc, BasicFontEnd - BasicFont
	call CopyToVRAM
	
	xor a
	ld [rVBK], a
	ret
	
	
